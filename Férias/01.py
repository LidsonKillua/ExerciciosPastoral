"""
Ler as dimensões de um retângulo, calcular e escrever a área
"""

base = float(input('Digite o valor da base do retângulo: '))
altura = float(input('Digite o valor da altura do retângulo: '))

area = base * altura

print(f'O triangulo de base {base:.2f}m e altura {altura:.2f}m tem área = {area:.2f}m².')
